<h4>Was kostet ein Sponsoring auf YiGG?</h4>
<p>
Für ein Sponsorpaket wird ein Fixpreis pro Monat berechnet. Die Höhe dieses Fixpreis ist abhängig von der gewählten Positionierung und variiert zwischen 29-199 Euro pro Monat.
Buchbar sind die Zeiträume: 1, 3 und 6 Monate. Für Buchungen von 3 und 6 Monaten gewähren wir Rabatt auf den Normalpreis, der im Buchungsprozess angezeigt wird.
</p>


<h4>Wie unterscheidet sich YiGG Sponsorings von klassischer Online-Werbung auf YiGG?</h4>
<p>
 YiGG Sponsor-Pakete können auf den unterschiedlichsten YiGG Seiten erworben werden. Dadurch erreiche ich als YiGG Sponsor meine Zielgruppe
 sehr effektiv. Ein YiGG Sponsor kann ich selbst buchen und aktivieren, ohne mich an das YiGG-Team oder einen Vermarkter wenden zu müssen.
 Die Preise für YiGG Sponsorings richten sich nicht nach einer übergeordneten Reichweite, sondern nach der Wertigkeit und Relevanz der einzelnen
 YiGG Seiten. Die Preise sind für jede YiGG Seite fix und gelten für jeweils 4 Wochen; danach kann ich sie als Sponsor verlängern.
</p>



